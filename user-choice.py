# User Choice Handling (Yes/No/Maybe)
user_input = input("Enter a word (yes/no/maybe): \n")
if user_input == "yes":
    print("You entered yes !")
elif user_input == "no":
    print("You entered no !")
elif user_input == "maybe":
    print("You entered maybe !")
else:
    print("Comply with the instructions !")
